// Mendefinisikan variabel
const angka = 10;

// Conditional if else
if (angka > 0) {
  console.log('Angka tersebut adalah positif');
} else {
  console.log('Angka tersebut adalah negatif atau nol');
}

// Mendefinisikan huruf
const huruf = 'a';

// Conditional if else untuk mengecek huruf vokal atau konsonan
if (
  huruf === 'a' ||
  huruf === 'e' ||
  huruf === 'i' ||
  huruf === 'o' ||
  huruf === 'u'
) {
  console.log(`${huruf} adalah huruf vokal`);
} else {
  console.log(`${huruf} adalah huruf konsonan`);
}
